<template>
  <dl>
    <dt>ID</dt>
    <dd>{{ user.id }}</dd>
    <dt>Name</dt>
    <dd>{{ user.name }}</dd>
  </dl>
</template>

<script>
import axios from 'axios';

export default {
  data: function () {
    return {
      user: {}
    }
  },
  mounted () {
    axios
      .get(`/api/v1/users/${this.$route.params.id}.json`)
      .then(response => (this.user = response.data))
  }
}
</script>

